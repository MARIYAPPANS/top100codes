"Python Program for Addition of two fractions"
n1 = int(input("Enter the numerator of the first fraction: "))
n2 = int(input("Enter the numerator of the second fraction: "))
m1 = int(input("Enter the denominator of the first fraction: "))
m2 = int(input("Enter the denominator of the second fraction: "))
result_numerator = (n1 * m2) + (n2 * m1)
result_denominator = m1 * m2
print("Sum of fractions:", result_numerator, "/", result_denominator)

